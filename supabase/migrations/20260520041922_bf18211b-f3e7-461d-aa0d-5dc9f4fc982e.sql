<p>This Agreement is between:</p>
<p>1. <strong>EventPix</strong> (a trading name of Backalley Production Company Pty Ltd, ABN 35 003 123 617) (<strong>EventPix</strong>, <strong>we</strong>, <strong>us</strong>); and</p>
<p>2. <strong>The Client</strong> identified above (<strong>Client</strong>, <strong>you</strong>).</p>

<h2>2. Sponsorship arrangement (value exchange)</h2>
<p><strong>2.1 Sponsored services:</strong> EventPix will provide event photography coverage as a sponsorship (in-kind).</p>
<p><strong>2.2 Sponsorship benefits:</strong> In exchange, the Client will provide the sponsorship benefits in clause 7 (Attendee list access) and clause 8 (Marketing opportunities), subject to clause 9 (Privacy and spam compliance).</p>

<h2>3. Photography in brief</h2>
<p>The EventPix photographer will photograph the event as directed by the event manager or as previously agreed.</p>

<h2>4. Coverage and deliverables</h2>
<p><strong>4.1 Coverage and delivery details:</strong> Coverage and delivery details are as agreed between the Parties.</p>
<p><strong>4.2 Delivery:</strong> EventPix will supply a copy of the images to the Client for the Client's use in accordance with this Agreement.</p>
<p><strong>4.3 Online gallery:</strong> Images may be made available for guests/delegates to access online via a dedicated URL/gallery platform. Unless otherwise agreed, the gallery remains available for 12 months.</p>

<h2>5. Public Liability, safety and site requirements</h2>
<p><strong>5.1 Insurance:</strong> All EventPix staff and sub-contractors are covered by our Public Liability policy.</p>
<p><strong>5.2 Direction onsite:</strong> When onsite, EventPix staff will follow all reasonable directions of the Event Manager appointed by the Client relating to site safety, security and venue rules.</p>
<p><strong>5.3 Inductions/briefings:</strong> The Client agrees to ensure that all site inductions for EventPix staff are carried out in accordance with industry practice.</p>

<h2>6. Copyright and licence</h2>
<p><strong>6.1 Copyright:</strong> All images produced by EventPix photographers are protected by Australian and international copyright. EventPix retains copyright in all images.</p>
<p><strong>6.2 Licence to Client:</strong> Subject to clause 6.4, EventPix grants the Client a royalty-free, non-exclusive and perpetual licence to use the delivered images for the Client's own <strong>non-commercial</strong> purposes (i.e. the images must not be resold, licensed or supplied for a fee without EventPix's written permission). Permitted uses include:</p>
<ul>
<li>digital use (e.g. website, social media, newsletters, PowerPoint presentations); and</li>
<li>hard copy use (e.g. print publications, prints for your own use).</li>
</ul>
<p><strong>6.3 Media use:</strong> The Client may supply images free of charge to electronic or print media for promotional or editorial use relating to the Event or Client. The Client must request the following credit where practicable: <strong>"Photo by EventPix"</strong>.</p>
<p><strong>6.4 Restrictions:</strong> Unless EventPix agrees in writing, the Client must not:</p>
<ul>
<li>sell, sub-license, or provide the images to third parties for their commercial use;</li>
<li>use the images in paid advertising or sponsor/partner advertising collateral (other than promoting the Event/Client) without written permission; or</li>
<li>apply heavy alteration/manipulation that changes the meaning/context of an image (basic cropping/resizing is permitted).</li>
</ul>
<p><strong>6.5 EventPix promotional use:</strong> EventPix may use selected images for EventPix's portfolio and promotional purposes (website, social channels, award submissions and capability decks), provided that:</p>
<ul>
<li>EventPix will not use any images of identified individuals who have opted out (if the Client has notified EventPix of opt-outs); and</li>
<li>EventPix will comply with any reasonable written restrictions notified by the Client before the Event (e.g. sensitive guests/areas).</li>
</ul>

<h2>7. Attendee list access (sponsorship benefit)</h2>
<p><strong>7.1 Provision of attendee list:</strong> The Client will provide EventPix with access to the Event attendee list (Attendee Data) as follows:</p>
<ul>
<li><strong>Fields:</strong> [e.g. first name, last name, email, organisation, role] (exclude phone unless agreed);</li>
<li><strong>Format:</strong> CSV or Excel</li>
</ul>
<p><strong>7.2 Purpose limitation:</strong> EventPix may use Attendee Data only for:</p>
<ul>
<li>delivering and administering access to event photos (e.g. gallery access, photo delivery), including sending operational/service messages required to provide gallery access; and</li>
<li><strong>not</strong> sending EventPix marketing messages to attendees, except where an attendee has separately opted in to receive marketing from EventPix (outside the Event registration), or where otherwise permitted under clause 9.</li>
</ul>
<p>The Parties acknowledge that EventPix's primary promotional benefit under this sponsorship is via the Client's marketing opportunities in clause 8 (e.g. a co-branded email sent by the Client), rather than EventPix contacting attendees directly.</p>
<p><strong>7.3 No sale:</strong> EventPix must not sell Attendee Data or disclose it to third parties (other than processors engaged to deliver the gallery/EDM, under confidentiality and only as required).</p>

<h2>8. Marketing opportunities (sponsorship benefit)</h2>
<p>The Client will provide marketing/promotional opportunities to EventPix such as:</p>
<ul>
<li><strong>Website:</strong> EventPix logo + link on the Event page;</li>
<li><strong>Social:</strong> posts and/or story posts tagging @EventPix (or [handle]);</li>
<li><strong>EDM (co-branded, sent by Client):</strong> inclusion in post-event email sent by the Client to attendees (e.g. "Photography sponsored by EventPix" + link to gallery and EventPix website). If the email includes any invitation to hear from EventPix, it must be structured as an <strong>opt-in</strong> (e.g. "Click here to subscribe to EventPix updates").</li>
<li><strong>Onsite acknowledgement:</strong> [signage / MC mention / screen slide]; and</li>
<li><strong>Optional:</strong> space for a small pull-up banner / table at registration area (if available).</li>
</ul>

<h2>9. Privacy, consent and spam compliance</h2>
<p><strong>9.1 Client warranty (notice/authority):</strong> The Client warrants that it is authorised to provide Attendee Data to EventPix and that attendees have been provided appropriate notice (and, where required by Privacy Laws, consent) that:</p>
<ul>
<li>their details may be disclosed to EventPix for the purposes of providing access to event photography (including operational/service emails or messages required to provide gallery access); and</li>
<li>the Client may send a co-branded post-event email that includes EventPix branding and links (clause 8).</li>
</ul>
<p><strong>9.2 No direct marketing by EventPix (default position):</strong> Unless an attendee has separately opted in, EventPix will not use Attendee Data to send marketing communications to attendees. If EventPix does send any commercial electronic message, EventPix must comply with the <em>Spam Act 2003 (Cth)</em> (including consent requirements where applicable and an unsubscribe facility).</p>
<p><strong>9.3 Client EDM compliance:</strong> The Client is responsible for compliance with Spam Laws for any emails/SMS it sends to attendees, including any co-branded EDM under clause 8.</p>
<p><strong>9.4 Opt-outs for photography:</strong> The Client is responsible for implementing a practical opt-out process for attendees who do not wish to be photographed (e.g. registration flagging, wristbands, or a nominated area), and for notifying EventPix of any opt-outs where practicable.</p>
<p><strong>9.5 Data security and retention:</strong> EventPix will take reasonable steps to protect Attendee Data and will delete or de-identify it when no longer required for the permitted purposes (unless required by law).</p>

<h2>10. Online distribution and gallery terms</h2>
<p><strong>10.1 Publishing for access:</strong> The Client agrees to allow EventPix to publish the images on an EventPix-controlled online gallery for the purpose of distributing digital files to guests/delegates and (if enabled) selling prints.</p>
<p><strong>10.2 Access:</strong> Guest access to online images will be via registration via a QR – guests enter contact details and a selfie. A URL may also be allocated to this Event. The Client will be responsible for distributing the link to this URL to whomsoever they see fit.</p>

<h2>11. Cancellation / postponement</h2>
<p><strong>11.1 Postponement:</strong> If the Event is postponed, the Parties will work together in good faith to reschedule the coverage to the new date (subject to photographer availability).</p>
<p><strong>11.2 Cancellation:</strong> If the Event is cancelled, the Client must reimburse any non-refundable, pre-approved out-of-pocket expenses incurred by EventPix (if any).</p>

